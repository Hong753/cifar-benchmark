class ViT_Tiny(nn.Module):
    def __init__(self, img_size, patch_size, embed_dim=192, num_layers=12, num_heads=3, num_classes=1000):
        super().__init__()

        # Patch embedding
        self.patch_embed = PatchEmbed(
            img_size=(img_size, img_size),
            patch_size=(patch_size, patch_size),
            in_channels=3,
            embed_dim=embed_dim,
            bias=True
        )
        num_patches = self.patch_embed.num_patches

        # Positional Embedding
        self.pos_embed = nn.Parameter(0.02 * torch.randn(1, num_patches, embed_dim))

        # CLS token
        self.cls_token = nn.Parameter(torch.zeros(1, 1, embed_dim))

        # Main blocks
        self.blocks = nn.Sequential(
            *[Block(embed_dim, num_heads) for _ in range(num_layers)]
        )

        # Output
        self.layer_norm = nn.LayerNorm(embed_dim)
        self.head = nn.Linear(embed_dim, num_classes)
        
        # Initialize weights
        trunc_normal_(self.cls_token, std=0.02)
        trunc_normal_(self.pos_embed, std=0.02)

    def forward(self, x):
        # Patchify: B x 3 x H x W -> B x T x C
        x = self.patch_embed(x)

        # Positional embedding: B x T x C
        x = x + self.pos_embed

        # Concat CLS token: B x (1 + T) x C
        cls_token = self.cls_token.expand(x.shape[0], -1, -1)
        x = torch.cat([cls_token, x], dim=1)

        # Main blocks: B x (1 + T) x C
        x = self.blocks(x)

        # Output: B x 1 x C -> B x num_classes
        x = x[:, 0] # CLS token only
        x = self.layer_norm(x)
        x = self.head(x)

        return x


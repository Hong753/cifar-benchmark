class MAE_Encoder(nn.Module):
    def __init__(self, img_size, patch_size, embed_dim=192, num_layers=12, num_heads=3, mask_ratio=0.75):
        super().__init__()
        
        # Conv stem
        self.conv_layers = nn.Sequential(
            nn.Conv2d(3, 48, kernel_size=3, stride=2, padding=1, bias=True), 
            nn.Conv2d(48, 96, kernel_size=3, stride=1, padding=1, bias=True), 
            nn.Conv2d(96, 192, kernel_size=3, stride=1, padding=1, bias=True), 
            nn.Conv2d(192, 192, kernel_size=3, stride=1, padding=1, bias=True)
        )
        
        # VQ
        self.vq = Quantizer(
            num_embeddings=1024, 
            embedding_dim=embed_dim, 
            commit_weight=1., 
            decay=0.99, 
            eps=1e-5, 
            threshold=2
        )

        # Patch embedding
        self.patch_embed = PatchEmbed(
            img_size=(img_size // 2, img_size // 2),
            patch_size=(patch_size, patch_size),
            in_channels=embed_dim,
            embed_dim=embed_dim,
            bias=True
        )
        num_patches = self.patch_embed.num_patches

        # Positional Embedding
        self.pos_embed = nn.Parameter(0.02 * torch.randn(1, num_patches, embed_dim))
        
        # Shuffle and mask patches
        self.patch_shuffle = PatchShuffle(mask_ratio)

        # CLS token
        self.cls_token = nn.Parameter(torch.zeros(1, 1, embed_dim))

        # Main blocks
        self.blocks = nn.Sequential(
            *[Block(embed_dim, num_heads) for _ in range(num_layers)]
        )

        # Output
        self.layer_norm = nn.LayerNorm(embed_dim)
        
        # Initialize weights
        trunc_normal_(self.cls_token, std=0.02)
        trunc_normal_(self.pos_embed, std=0.02)

    def forward(self, x):
        # Conv stem: B x 3 x H x W -> B x C x H' x W'
        x = self.conv_layers(x)
        
        # Quantize: B x C x H' x W' -> B x C x H' x W'
        x, commit_loss = self.vq(x)
        
        # Patchify: B x C x H' x W' -> B x T x C
        x = self.patch_embed(x)

        # Positional embedding: B x T x C
        x = x + self.pos_embed
        
        # Shuffle and mask patches: B x T x C -> B x t x C
        x, forward_indexes, backward_indexes = self.patch_shuffle(x)

        # Concat CLS token: B x (1 + t) x C
        cls_token = self.cls_token.expand(x.shape[0], -1, -1)
        x = torch.cat([cls_token, x], dim=1)

        # Main blocks: B x (1 + t) x C
        x = self.blocks(x)

        # Output: B x (1 + t) x C
        x = self.layer_norm(x)

        return x, backward_indexes, commit_loss


class VQMAE(nn.Module):
    def __init__(self, img_size, patch_size, embed_dim=192, num_layers=12, num_heads=3, mask_ratio=0.75):
        super().__init__()
        
        common_kwargs = {"img_size": img_size, "patch_size": patch_size, "embed_dim": embed_dim, "num_heads": num_heads}
        self.encoder = MAE_Encoder(num_layers=num_layers, mask_ratio=mask_ratio, **common_kwargs)
        self.decoder = MAE_Decoder(num_layers=num_layers // 3, **common_kwargs)
        
    def forward(self, x):
        x, backward_indexes, commit_loss = self.encoder(x)
        pred_images, mask = self.decoder(x, backward_indexes)
        return pred_images, mask, commit_loss
    
    @property
    def count(self):
        return (self.encoder.vq.count > 0).sum()
    
    def reset_count(self):
        self.encoder.vq.reset_count()
    

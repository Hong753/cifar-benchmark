class PatchShuffle(nn.Module):
    def __init__(self, ratio):
        super().__init__()
        self.ratio = ratio
        
    @staticmethod
    def random_indexes(size):
        forward_indexes = np.arange(size)
        np.random.shuffle(forward_indexes)
        backward_indexes = np.argsort(forward_indexes)
        return forward_indexes, backward_indexes
        
    def forward(self, patches):
        B, T, C = patches.shape
        device = patches.device
        T_out = int(T * (1 - self.ratio))
        
        forward_indexes, backward_indexes = [], []
        for _ in range(B):
            f_idx, b_idx = self.random_indexes(T)
            forward_indexes += [torch.as_tensor(f_idx, dtype=torch.long)]
            backward_indexes += [torch.as_tensor(b_idx, dtype=torch.long)]
        forward_indexes = torch.stack(forward_indexes, dim=0).to(device)
        backward_indexes = torch.stack(backward_indexes, dim=0).to(device)
        
        patches = take_indexes(patches, forward_indexes)
        patches = patches[:, :T_out]
        
        return patches, forward_indexes, backward_indexes


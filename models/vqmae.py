class Quantizer(nn.Module):
    def __init__(self, num_embeddings, embedding_dim, commit_weight, decay=0.99, eps=1e-5, threshold=2):
        super().__init__()
        self.num_embeddings = num_embeddings
        self.embedding_dim = embedding_dim
        self.commit_weight = commit_weight
        self.decay = decay
        self.eps = eps
        self.threshold = threshold
        
        # Embeddings
        # m = torch.distributions.half_normal.HalfNormal(scale=1.0)
        # embeddings = F.normalize(m.sample([num_prototypes, embedding_dim]), dim=1)
        embeddings = torch.randn(num_embeddings, embedding_dim)
        embeddings = F.normalize(embeddings, dim=1)
        self.register_buffer("embeddings", embeddings)
        self.register_buffer("embeddings_ema", embeddings.clone())
        self.register_buffer("cluster_size", torch.zeros(num_embeddings))
        self.register_buffer("count", torch.zeros(num_embeddings))
        
    def quantize(self, x):
        assert x.ndim == 2
        assert x.shape[1] == self.embedding_dim
        embeddings = self.embeddings.to(torch.float32)
        
        dist = x @ embeddings.T
        q_idx = dist.argmax(dim=1)
        q = embeddings[q_idx]
        return q, q_idx
    
    def forward(self, x):
        # B x C x H x W -> BHW x C
        x = x.permute(0, 2, 3, 1).contiguous()
        B, H, W, C = x.shape
        x = x.reshape(-1, C)
        
        # Normalize
        x = F.normalize(x, dim=1)
        
        # Quantize codes
        q, q_idx = self.quantize(x)
        q_onehot_idx = F.one_hot(q_idx, num_classes=self.num_embeddings).to(torch.float32)
        self.count += torch.sum(q_onehot_idx, dim=0)
        
        # Update codes
        if self.training:
            with torch.no_grad():
                n_k = torch.sum(q_onehot_idx, dim=0)
                self.cluster_size = self.cluster_size * self.decay + n_k * (1 - self.decay)
                embeddings_sum = (q_onehot_idx.T @ x)
                self.embeddings_ema = self.embeddings_ema * self.decay + embeddings_sum * (1 - self.decay)
                n = torch.sum(self.cluster_size)
                cluster_size = (self.cluster_size + self.eps) / (n + self.num_embeddings * self.eps) * n
                new_embeddings = self.embeddings_ema / cluster_size.unsqueeze(dim=1)
                self.embeddings = F.normalize(new_embeddings, dim=1)
                self.expire_embeddings(x)
        
        # Commitment loss and straight-through estimator
        commit_loss = F.mse_loss(x, q.detach())
        q = x + (q - x).detach()
        
        # BHW x C -> B x C x H x W
        q = q.reshape(B, H, W, C)
        q = q.permute(0, 3, 1, 2).contiguous()
        
        return q, self.commit_weight * commit_loss
    
    def expire_embeddings(self, x):
        expired_idx = self.cluster_size < self.threshold
        
        if not torch.any(expired_idx):
            return
        
        num_samples = x.shape[0]
        num_expired = expired_idx.sum()
        if num_samples > num_expired:
            random_idx = torch.randperm(num_samples, device=x.device)[:num_expired]
        else:
            random_idx = torch.randint(0, num_samples, size=(num_expired, ), device=x.device)
        random_x = x[random_idx]
        self.embeddings.data[expired_idx] = random_x
        self.embeddings_ema.data[expired_idx] = self.threshold * random_x
        self.cluster_size[expired_idx] = self.threshold
        self.count[expired_idx] = 0
    
    def reset_count(self):
        self.count = torch.zeros(self.num_embeddings, device=self.count.device)


class MAE_Decoder(nn.Module):
    def __init__(self, img_size, patch_size, embed_dim=192, num_layers=4, num_heads=3):
        super().__init__()
        
        # Mask token
        self.mask_token = nn.Parameter(torch.zeros(1, 1, embed_dim))
        
        # Positional Embedding
        num_patches = 1 + (img_size // patch_size) ** 2
        self.pos_embed = nn.Parameter(0.02 * torch.randn(1, num_patches, embed_dim))

        # Main blocks
        self.blocks = nn.Sequential(
            *[Block(embed_dim, num_heads) for _ in range(num_layers)]
        )

        # Output
        self.head = nn.Linear(embed_dim, 3 * patch_size ** 2)
        self.patch2img = Rearrange('b (h w) (c p1 p2) -> b c (h p1) (w p2)', p1=patch_size, p2=patch_size, h=img_size//patch_size)
        
        # Initialize weights
        trunc_normal_(self.mask_token, std=0.02)
        trunc_normal_(self.pos_embed, std=0.02)

    def forward(self, x, backward_indexes):
        t = x.shape[1] - 1
        B, T = backward_indexes.shape
        
        # Concat mask tokens: B x (1 + t) x C -> B x (1 + T) x C
        mask_tokens = self.mask_token.expand(B, T - t, -1)
        x = torch.cat([x, mask_tokens], dim=1)
        
        # Concat CLS token index: B x T -> B x (1 + T)
        cls_token_index = torch.zeros(B, 1).to(backward_indexes)
        backward_indexes = torch.cat([cls_token_index, backward_indexes + 1], dim=1)
        
        # Unshuffle patches: B x (1 + T) x C
        x = take_indexes(x, backward_indexes)
        
        # Positional embedding: B x (1 + T) x C
        x = x + self.pos_embed
        
        # Main blocks: B x (1 + T) x C
        x = self.blocks(x)
        
        # Remove CLS token: B x (1 + T) x C -> B x T x C
        x = x[:, 1:]

        # Output: B x T x C -> B x T x (3 x P x P) -> B x 3 x H x W
        x = self.head(x)
        pred_images = self.patch2img(x)
        
        # Mask: B x T x (3 x P x P) -> B x 3 x H x W
        mask = torch.zeros_like(x)
        mask[:, t:] = 1 # 1 indicates masked, 0 otherwise
        mask = take_indexes(mask, backward_indexes[:, 1:] - 1)
        mask = self.patch2img(mask)

        return pred_images, mask


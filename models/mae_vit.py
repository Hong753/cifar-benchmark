class MAE_ViT_Tiny(nn.Module):
    def __init__(self, img_size, patch_size, embed_dim=192, num_layers=12, num_heads=3, mask_ratio=0.75, num_classes=1000):
        super().__init__()
        
        common_kwargs = {"img_size": img_size, "patch_size": patch_size, "embed_dim": embed_dim, "num_heads": num_heads}
        self.encoder = MAE_Encoder(num_layers=num_layers, mask_ratio=mask_ratio, **common_kwargs)
        self.decoder = MAE_Decoder(num_layers=num_layers // 3, **common_kwargs)
        self.head = nn.Linear(embed_dim, num_classes)
        
    def forward(self, x, skip_mask=False):
        if skip_mask:
            x = self.encoder(x, skip_mask=skip_mask)
            x = self.head(x)
            return x
        
        x, backward_indexes = self.encoder(x, skip_mask=skip_mask)
        pred_images, mask = self.decoder(x, backward_indexes)
        return pred_images, mask
    
